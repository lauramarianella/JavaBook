package MyPkg.CH_12.ExceptionHandlingAndTextIO;


public class _12_10_InvalidRadiusException extends Exception {
  private double radius;

  /** Construct an exception */
  public _12_10_InvalidRadiusException(double radius) {
    super("Invalid radius " + radius);
    this.radius = radius;
  }

  /** Return the radius */
  public double getRadius() {
    return radius;
  }
}
